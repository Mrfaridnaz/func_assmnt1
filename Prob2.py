# Can a lambda function in Python have multiple arguments? If yes, how can you define and use
# them?
# Yes, Explaination in text2

# Lambda function with two arguments
multiply = lambda x, y: x * y
result = multiply(5, 10)
print(result)  # Output: 50


# Lambda function with three arguments
sum_three_numbers = lambda a, b, c: a + b + c
result = sum_three_numbers(1, 2, 3)
print(result)  # Output: 6




